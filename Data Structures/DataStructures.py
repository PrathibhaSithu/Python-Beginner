# List (Array)
fruits = ["apple", "banana", "cherry"]
print(fruits)

# Accessing elements
print(fruits[0])  # Output: apple
print(fruits[1])  # Output: banana
print(fruits[2])  # Output: cherry

# Adding elements
fruits.append("orange")
print(fruits)

# List Slicing
print(fruits[1:3])

# Removing elements
fruits.remove("banana")
print(fruits)

