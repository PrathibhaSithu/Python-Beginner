import math

print(math.sqrt(16))  # Square root of 16
print(math.ceil(2.3))  # Round up to nearest integer # Output: 3
print(math.floor(2.7))  # Round down to nearest integer # Output: 2
print(math.pi)  # Value of pi # Output: 3.141592653589793
print(math.e)  # Value of e # Output: 2.718281828459281828459045
print(math.pow(2, 3))  # 2 raised to the power of
